import { createHash } from 'node:crypto'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { DOC_SECTION_ORDER, METADATA_MAPPING_SPEC, MISSING_METADATA_TEXT } from './metadata-mapping-spec.mjs'

function unique(values) {
  return [...new Set(values)]
}

function readFirstJsonString(source, key) {
  const regex = new RegExp(`"${key}":"([^"]*)"`)
  const match = source.match(regex)
  if (!match) return null
  try {
    return JSON.parse(`"${match[1]}"`)
  } catch {
    return match[1]
  }
}

function readAllJsonStrings(source, key) {
  const regex = new RegExp(`"${key}":"([^"]*)"`, 'g')
  const items = []
  let match
  while ((match = regex.exec(source)) !== null) {
    try {
      items.push(JSON.parse(`"${match[1]}"`))
    } catch {
      items.push(match[1])
    }
  }
  return unique(items)
}

function countOccurrences(source, token) {
  const escaped = token.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const regex = new RegExp(escaped, 'g')
  return (source.match(regex) || []).length
}

function sha256(content) {
  return createHash('sha256').update(content).digest('hex')
}

function asPosixPath(filePath) {
  return filePath.replace(/\\/g, '/')
}

function toImportPath(fromFile, toFile) {
  const rel = asPosixPath(path.relative(path.dirname(fromFile), toFile)).replace(/\.ts$/, '.js')
  return rel.startsWith('.') ? rel : `./${rel}`
}

function buildDocSections(symbol, sourceRelPath, sourceText) {
  const title = readFirstJsonString(sourceText, 'title') || symbol
  const description = readFirstJsonString(sourceText, 'description') || MISSING_METADATA_TEXT
  const formats = readAllJsonStrings(sourceText, 'format')
  const patterns = readAllJsonStrings(sourceText, 'pattern')

  const bounds = {
    minimum: countOccurrences(sourceText, '"minimum":'),
    maximum: countOccurrences(sourceText, '"maximum":'),
    exclusiveMinimum: countOccurrences(sourceText, '"exclusiveMinimum":'),
    exclusiveMaximum: countOccurrences(sourceText, '"exclusiveMaximum":'),
    minLength: countOccurrences(sourceText, '"minLength":'),
    maxLength: countOccurrences(sourceText, '"maxLength":'),
    minItems: countOccurrences(sourceText, '"minItems":'),
    maxItems: countOccurrences(sourceText, '"maxItems":')
  }

  const enumCount = countOccurrences(sourceText, 'Type.Literal(')
  const defaultCount = countOccurrences(sourceText, '"default":')
  const examplesCount = countOccurrences(sourceText, '"examples":') + countOccurrences(sourceText, '"example":')
  const requiredCount = countOccurrences(sourceText, '"required":')
  const deprecatedCount = countOccurrences(sourceText, '"deprecated":')
  const readOnlyCount = countOccurrences(sourceText, '"readOnly":')
  const writeOnlyCount = countOccurrences(sourceText, '"writeOnly":')

  return {
    summary: title,
    description,
    constraints: `numeric bounds markers: min=${bounds.minimum}, max=${bounds.maximum}, exMin=${bounds.exclusiveMinimum}, exMax=${bounds.exclusiveMaximum}; size markers: minLength=${bounds.minLength}, maxLength=${bounds.maxLength}, minItems=${bounds.minItems}, maxItems=${bounds.maxItems}; required markers=${requiredCount}`,
    enum: enumCount > 0 ? `literal options detected: ${enumCount}` : 'literal options not detected',
    default: defaultCount > 0 ? `default markers detected: ${defaultCount}` : 'default markers not detected',
    examples: examplesCount > 0 ? `example markers detected: ${examplesCount}` : 'example markers not detected',
    format: formats.length > 0 ? `formats: ${formats.join(', ')}` : 'formats not detected',
    pattern: patterns.length > 0 ? `patterns: ${patterns.join(', ')}` : 'patterns not detected',
    provenance: `source: ${sourceRelPath}`,
    lifecycle: `deprecated markers=${deprecatedCount}, readOnly markers=${readOnlyCount}, writeOnly markers=${writeOnlyCount}`
  }
}

function renderDocComment(sections) {
  const lines = ['/**']
  for (const key of DOC_SECTION_ORDER) {
    lines.push(` * ${key}: ${sections[key]}`)
  }
  lines.push(' */')
  return lines.join('\n')
}

function renderHelperModule(entries, { repoRoot, helperFile }) {
  const lines = [
    '// Generated by converter/app/publish/generate-intellisense-helpers.mjs - do not edit manually',
    ''
  ]

  for (const entry of entries) {
    const sourceAbsPath = path.join(repoRoot, entry.sourceRelPath)
    const importPath = toImportPath(helperFile, sourceAbsPath)
    lines.push(renderDocComment(entry.sections))
    lines.push(`export type ${entry.symbol} = import('${importPath}').${entry.symbol}`)
    lines.push('')
  }

  return lines.join('\n')
}

/**
 * Generate per-group IntelliSense sidecar files and a manifest.
 *
 * @param {object} opts
 * @param {string} opts.repoRoot          - Absolute repo root
 * @param {Map<string,string>} opts.symbolIndex  - symbol → repo-relative source path
 * @param {string} opts.outputDir         - Where to write generated files (e.g. converter/app/intellisenseOutput)
 * @param {string} opts.libIntellisenseRoot - Repo-relative destination path used to compute correct import paths
 *                                          (e.g. src/lib/intellisense) — files are PROMOTED here after generation
 * @param {Record<string, string[]>} opts.facadeSpec - Group name → symbol list (same as FACADE_SPEC in publish.mjs)
 */
export async function generateIntellisenseHelpers({ repoRoot, symbolIndex, outputDir, diagnosticsDir, libIntellisenseRoot, facadeSpec }) {
  await mkdir(outputDir, { recursive: true })
  const resolvedDiagnosticsDir = diagnosticsDir ?? outputDir
  await mkdir(resolvedDiagnosticsDir, { recursive: true })

  // Pre-load unique symbols (a symbol can appear in multiple groups)
  const entryCache = new Map()
  for (const symbols of Object.values(facadeSpec)) {
    for (const symbol of symbols) {
      if (entryCache.has(symbol)) continue
      const sourceRelPath = symbolIndex.get(symbol)
      if (!sourceRelPath) continue
      const sourceAbsPath = path.join(repoRoot, sourceRelPath)
      const sourceText = await readFile(sourceAbsPath, 'utf8')
      const sections = buildDocSections(symbol, sourceRelPath, sourceText)
      entryCache.set(symbol, { symbol, sourceRelPath, sourceHash: sha256(sourceText), sections })
    }
  }

  const groupHashes = {}

  for (const [group, symbols] of Object.entries(facadeSpec)) {
    const entries = symbols.filter((sym) => entryCache.has(sym)).map((sym) => entryCache.get(sym))

    // Use the DESTINATION path (src/lib/intellisense/{group}.ts) so import paths
    // remain correct after promotion — the generated file is copied there verbatim.
    const destFile = path.join(repoRoot, libIntellisenseRoot, `${group}.ts`)
    const groupSource = renderHelperModule(entries, { repoRoot, helperFile: destFile })
    const outFile = path.join(outputDir, `${group}.ts`)
    await writeFile(outFile, groupSource, 'utf8')
    groupHashes[group] = sha256(groupSource)
  }

  // Index barrel — deduplicate symbols that appear in multiple groups.
  // Per-group files remain complete (useful for direct subpath imports),
  // but the barrel uses explicit named exports to avoid ambiguity.
  const seenSymbols = new Set()
  const indexLines = [
    '// Generated by converter/app/publish/generate-intellisense-helpers.mjs - do not edit manually',
    ''
  ]
  for (const [group, symbols] of Object.entries(facadeSpec)) {
    const uniqueSymbols = symbols.filter((sym) => entryCache.has(sym) && !seenSymbols.has(sym))
    if (uniqueSymbols.length === 0) continue
    const typeExports = uniqueSymbols.join(', ')
    indexLines.push(`export type { ${typeExports} } from './${group}.js'`)
    for (const sym of uniqueSymbols) seenSymbols.add(sym)
  }
  const indexSource = `${indexLines.join('\n')}\n`
  await writeFile(path.join(outputDir, 'index.ts'), indexSource, 'utf8')

  // Manifest
  const manifestEntries = [...entryCache.values()]
    .sort((a, b) => a.symbol.localeCompare(b.symbol))
    .map((entry) => ({
      symbol: entry.symbol,
      sourceRelPath: entry.sourceRelPath,
      sourceHash: entry.sourceHash,
      coverage: {
        hasDescription: entry.sections.description !== MISSING_METADATA_TEXT,
        hasFormat: entry.sections.format !== 'formats not detected',
        hasPattern: entry.sections.pattern !== 'patterns not detected'
      }
    }))

  const manifest = {
    version: 1,
    generatedAt: '1970-01-01T00:00:00.000Z',
    mappingSpec: {
      sectionOrder: DOC_SECTION_ORDER,
      fields: METADATA_MAPPING_SPEC,
      missingMetadataText: MISSING_METADATA_TEXT
    },
    entries: manifestEntries
  }

  const manifestFile = path.join(resolvedDiagnosticsDir, 'intellisense-manifest.json')
  const manifestSource = JSON.stringify(manifest, null, 2)
  await writeFile(manifestFile, manifestSource, 'utf8')

  const unresolvedMetadata = [...entryCache.values()].filter(
    (entry) => entry.sections.description === MISSING_METADATA_TEXT
  ).length

  return {
    outputDir,
    manifestFile,
    groupHashes,
    indexHash: sha256(indexSource),
    manifestHash: sha256(manifestSource),
    symbolCount: entryCache.size,
    groupCount: Object.keys(facadeSpec).length,
    unresolvedMetadata
  }
}
